# List Methods
list1 = [10, 20, 30]

# Append
list1.append(40)
print("After Append:", list1)

# Extend
list1.extend([50, 60])
print("After Extend:", list1)

# Insert
list1.insert(1, 15)
print("After Insert:", list1)

# Delete
list1.remove(30)
print("After Remove:", list1)

# Pop
list1.pop()
print("After Pop:", list1)

# Clear
list1.clear()
print("After Clear:", list1)
